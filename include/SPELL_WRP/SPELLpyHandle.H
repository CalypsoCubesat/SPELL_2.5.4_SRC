//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Variable value implementation
 **
 *////////////////////////////////////////////////////////////////////////////
class SPELLpyHandle
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor
		**////////////////////////////////////////////////////////////////////
		SPELLpyHandle( PyObject* pyObject )
		{
			m_pyObject = pyObject;
		}

		//////////////////////////////////////////////////////////////////////
		/** Destructor
		**////////////////////////////////////////////////////////////////////
		~SPELLpyHandle()
		{
			Py_XDECREF(m_pyObject);
			m_pyObject = NULL;
		}

	// OPERATORS /////////////////////////////////////////////////////////////

		SPELLpyHandle& operator=( PyObject* pyObject )
		{
			if (m_pyObject != NULL)
			{
				Py_XDECREF(m_pyObject);
			}
			m_pyObject = pyObject;
			return *this;
		}

	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Set variable value
		**////////////////////////////////////////////////////////////////////
		void set( PyObject* pyObject )
		{
			if (m_pyObject != NULL)
			{
				Py_XDECREF(m_pyObject);
			}
			m_pyObject = pyObject;
		}

		//////////////////////////////////////////////////////////////////////
		/** Get variable value
		**////////////////////////////////////////////////////////////////////
		PyObject* get() const
		{
			return m_pyObject;
		}

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////
    // DATA MEMBERS //////////////////////////////////////////////////////////

		PyObject* m_pyObject;
};


